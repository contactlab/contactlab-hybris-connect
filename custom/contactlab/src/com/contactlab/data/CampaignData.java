package com.contactlab.data;

public class CampaignData
{
    private int id;

    private String label;

    private String webFormCode;

    public int getId()
    {
        return id;
    }

    public void setId(final int id)
    {
        this.id = id;
    }

    public String getLabel()
    {
        return label;
    }

    public void setLabel(final String label)
    {
        this.label = label;
    }

    public String getWebFormCode()
    {
        return webFormCode;
    }

    public void setWebFormCode(final String webFormCode)
    {
        this.webFormCode = webFormCode;
    }
}
